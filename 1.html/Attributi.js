//todo globali (possono essere usati su quasi tutti i tag)
// id //* Identificatore unico dell’elemento
// class //* Classe CSS per stile o selezione JS
// style //* Stile inline dell’elemento
// title //* Testo mostrato come tooltip
// hidden //* Nasconde l’elemento
// contenteditable //* Rende modificabile il contenuto
// tabindex //* Controlla l’ordine di tabulazione
// data-* //* Attributi personalizzati per dati custom

//todo link e immagini
// href //* URL del link
// src //* URL di immagine, video, audio
// alt //* Testo alternativo per immagini
// width //* Larghezza (px o %)
// height //* Altezza (px o %)
// crossorigin //* Gestione CORS per media

//todo form
// type //* Tipo di input (text, password, submit…)
// name //* Nome del campo per form submission
// value //* Valore iniziale del campo
// placeholder //* Testo mostrato quando il campo è vuoto
// required //* Campo obbligatorio
// disabled //* Campo disabilitato
// checked //* Stato iniziale di checkbox o radio
// maxlength //* Lunghezza massima del testo
// minlength //* Lunghezza minima del testo
// pattern //* Regex per validazione del valore
// multiple //* Permette selezione multipla (file/select)

//todo media
// autoplay //* Avvia automaticamente video/audio
// controls //* Mostra controlli player
// loop //* Ripete automaticamente media
// muted //* Inizialmente muto
// poster //* Immagine di anteprima per video

//todo tabella
// colspan //* Numero di colonne da unire
// rowspan //* Numero di righe da unire
// headers //* Collega celle ai loro header
// scope //* Definisce se la cella è header per row/col

//todo globali ARIA/accessibilità
// role //* Ruolo dell’elemento per screen reader
// aria-* //* Attributi ARIA per accessibilità (label, hidden, expanded…)
