// .class //* Seleziona tutti gli elementi con quella classe
// #id //* Seleziona l’elemento con quell’id
// element, element //* Seleziona più elementi separati da virgola
// element element //* Seleziona elementi figli discendenti di un altro elemento
// element > element //* Seleziona elementi figli diretti
// element + element //* Seleziona l’elemento immediatamente successivo
// element ~ element //* Seleziona elementi fratelli successivi
// [attribute] //* Seleziona elementi che hanno un certo attributo
// [attribute=value] //* Seleziona elementi con attributo uguale a un valore
// [attribute~=value] //* Seleziona elementi con attributo contenente una parola
// [attribute|=value] //* Seleziona elementi con attributo che inizia con valore seguito da "-" 

